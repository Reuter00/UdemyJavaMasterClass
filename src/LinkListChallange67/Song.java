package LinkListChallange67;

public class Song {
    private String title;
    private double duratio;

    public Song(String title, double duratio) {
        this.title = title;
        this.duratio = duratio;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public double getDuratio() {
        return duratio;
    }

    public void setDuratio(double duratio) {
        this.duratio = duratio;
    }



}
